    @classmethod
    def weapons_in_category(cls, category_id: str) -> tuple[str, ...]:
        if category_id == 'smg':
            return cls.SMG_WEAPONS
        return cls.RIFLE_WEAPONS

    @classmethod
    def team_for_weapon(cls, weapon_id: str) -> str:
        if weapon_id in cls.BOTH_WEAPONS:
            return 't'
        if weapon_id in cls.T_WEAPONS:
            return 't'
        return 'ct'

    @classmethod
    def weapons_in_team_category(cls, team: str, category_id: str) -> tuple[str, ...]:
        pool = cls.weapons_in_category(category_id)
        side = cls.T_WEAPONS if team == 't' else cls.CT_WEAPONS
        return tuple(
            weapon_id
            for weapon_id in pool
            if weapon_id in cls.BOTH_WEAPONS or weapon_id in side
        )

    @classmethod
    def category_for_weapon(cls, weapon_id: str) -> str:
        if weapon_id in cls.SMG_WEAPONS:
            return 'smg'
        return 'rifle'

    @classmethod
    def get_profile(cls, weapon_id: str) -> str:
        return cls.WEAPON_PROFILES.get(weapon_id, 'smooth')

    @classmethod
    def apply_tail_tune(
        cls,
        weapon_id: str,
        step_index: int,
        pattern_len: int,
        x: float,
        y: float,
    ) -> tuple[float, float]:
        tune = cls.TAIL_TUNE.get(weapon_id)
        if not tune or pattern_len <= 0:
            return x, y
        start = int(pattern_len * tune['from_fraction'])
        if step_index < start:
            return x, y
        return x * tune.get('x_scale', 1.0), y * tune.get('y_scale', 1.0)

    @classmethod
    def get_pattern(cls, weapon_id: str) -> list[tuple[float, float, float]]:
        data = cls()
        pattern = getattr(data, weapon_id, None)
        if isinstance(pattern, list) and pattern:
            return pattern
        return data.assault_rifle
